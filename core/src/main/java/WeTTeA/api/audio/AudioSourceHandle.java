package WeTTeA.api.audio;

/**
 * Дескриптор одного активного звукового источника, которым владеет backend.
 *
 * <p>Контракт:
 * <ul>
 *   <li>дескриптор валиден до явного {@link #stop()} либо {@link #dispose()},
 *       либо до естественного окончания не-loop звука (тогда backend
 *       помечает дескриптор как expired);</li>
 *   <li>повторные вызовы методов после остановки — no-op;</li>
 *   <li>дескриптор не несёт состояния — backend хранит реальное состояние
 *       у себя, дескриптор передаёт ему id.</li>
 * </ul>
 *
 * @author Kuruma
 * @since 0.1.0
 */
public interface AudioSourceHandle {

    /** Уникальный id внутри backend'а. Можно логировать, не передавать наружу. */
    long id();

    /** Категория, к которой относится источник. */
    AudioCategory category();

    /** Источник ещё активен (играет или приостановлен). */
    boolean isActive();

    /** Установить громкость в диапазоне {@code [0..1]}. */
    void setVolume(float volume);

    /** Установить pitch (1.0 = нормальный). */
    void setPitch(float pitch);

    /** Сделать loop / убрать loop. */
    void setLooping(boolean looping);

    /** Приостановить воспроизведение, сохранив позицию. */
    void pause();

    /** Возобновить с приостановленной позиции. */
    void resume();

    /** Полностью остановить источник. */
    void stop();

    /** Освободить дескриптор. После этого вызывать методы запрещено. */
    void dispose();
}
