package WeTTeA.api.lifecycle;

/**
 * Объект, обновляющийся каждый тик игрового цикла.
 *
 * <p><b>Update / render разделение.</b> Реализации обязаны выполнять только
 * <b>симуляцию</b> состояния (физика, AI, бизнес-логика, таймеры).
 * Запрещено в {@link #tick(double)}:
 * <ul>
 *   <li>обращаться к render API (Vulkan/OpenGL handles);</li>
 *   <li>создавать GPU ресурсы;</li>
 *   <li>записывать команды в command buffer.</li>
 * </ul>
 * Эта работа делается в реализациях {@link Drawable}.
 *
 * <p><b>Производительность.</b> Метод вызывается покадрово (минимум 60 раз
 * в секунду на desktop, 30 на слабых mobile). Запрещены лишние аллокации
 * в горячем пути — они создают GC pressure при 80k+ обновляемых объектов.
 *
 * @author Kuruma
 * @since 0.1.0
 */
@FunctionalInterface
public interface Tickable {

    /**
     * Обновить состояние объекта на один шаг симуляции.
     *
     * @param deltaSeconds время с прошлого вызова, в секундах. Гарантированно положительное.
     *                     При фиксированном шаге равно фиксированной величине шага.
     */
    void tick(double deltaSeconds);
}
