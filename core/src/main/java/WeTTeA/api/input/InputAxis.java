package WeTTeA.api.input;

/**
 * Аналоговые оси ввода — стик, мышь, акселерометр, gamepad triggers.
 *
 * <p>Используется для непрерывных значений в диапазоне:
 * <ul>
 *   <li>{@code [-1.0 .. 1.0]} для двунаправленных осей (стик, мышь);</li>
 *   <li>{@code [0.0 .. 1.0]} для однонаправленных (триггеры).</li>
 * </ul>
 *
 * @author Kuruma
 * @since 0.1.0
 */
public enum InputAxis {

    /** Горизонтальное движение игрока. {@code -1} = влево, {@code 1} = вправо. */
    MOVE_X,

    /** Вертикальное движение игрока (вперёд/назад в 3D, вверх/вниз в 2D). */
    MOVE_Y,

    /** Поворот камеры по горизонтали. */
    CAMERA_X,

    /** Поворот камеры по вертикали. */
    CAMERA_Y,

    /** Зум камеры. */
    CAMERA_ZOOM,

    /** Триггер первичной атаки (force на gamepad). {@code [0..1]}. */
    ATTACK_PRIMARY_FORCE,

    /** Триггер вторичной атаки. {@code [0..1]}. */
    ATTACK_SECONDARY_FORCE
}
