package WeTTeA.api.render;

/**
 * Пресеты качества рендера.
 *
 * <p>Используется в {@code GameConfig} и UI настроек как единая enum-ось,
 * к которой привязаны конкретные параметры (разрешение теней, MSAA,
 * количество отражений, dynamic resolution коэффициент, дальность LOD,
 * лимиты пост-процессинга).
 *
 * <p>Сами параметры под каждым tier'ом задаются в JSON конфигах
 * ({@code assets/death/data/quality/}), а <b>не</b> хардкодятся в коде —
 * чтобы можно было менять профили без перекомпиляции.
 *
 * @author Kuruma
 * @since 0.1.0
 */
public enum RenderQualityTier {

    /** Ультра-низкие настройки — максимально совместимый mobile/старый desktop. */
    POTATO,

    /** Низкие — стабильные 60 fps на средней конфигурации mobile. */
    LOW,

    /** Средние — основная цель для среднего desktop / топ-мобайла. */
    MEDIUM,

    /** Высокие — современный desktop / Apple silicon / топ-Android. */
    HIGH,

    /** Ультра — высокие настройки для desktop с дискретной видеокартой. */
    ULTRA
}
